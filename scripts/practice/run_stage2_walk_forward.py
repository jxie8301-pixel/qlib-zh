def _train_single_model_fold(
    spec: dict[str, object],
    fold: dict[str, str],
    fold_dir: Path,
    mlruns_root: Path,
    base_env: dict[str, str],
    uri_folder: str,
    experiment_name: str,
    half_life: int,
    floor: float,
    train_base_start: str,
    warm_start_checkpoint: Path | None = None,
) -> tuple[Path, str]:
    model_name, model_dir, fold_config = _generate_model_fold_config(
        spec=spec,
        fold=fold,
        fold_dir=fold_dir,
        base_env=base_env,
        half_life=half_life,
        train_base_start=train_base_start,
    )

    existing_run_dir = model_dir / "mlflow_run"
    existing_artifacts = existing_run_dir / "artifacts"
    required_artifacts = [
        existing_artifacts / "pred.pkl",
        existing_artifacts / "label.pkl",
        existing_artifacts / "valid_pred.pkl",
        existing_artifacts / "valid_label.pkl",
        existing_artifacts / "params.pkl",
    ]
    if all(path.exists() for path in required_artifacts):
        print(f"      reuse cached run: {model_name} -> {existing_run_dir}")
        return existing_run_dir, f"{experiment_name}_wf_{fold['signal_date']}_{model_name}"

    experiment_tag = f"{experiment_name}_wf_{fold['signal_date']}_{model_name}"
    start_ts = time.time()
    if warm_start_checkpoint is not None:
        print(f"      warm-start: {model_name} <- {warm_start_checkpoint}")
    else:
        print(f"      warm-start: {model_name} <- <none>")
    _run(
        [
            sys.executable,
            str(SCRIPTS / "run_stage2_practice.py"),
            "--config",
            str(fold_config),
            "--experiment-name",
            experiment_tag,
            "--uri-folder",
            uri_folder,
            "--half-life",
            str(half_life),
            "--floor",
            str(floor),
            *(["--warm-start", str(warm_start_checkpoint)] if warm_start_checkpoint is not None else []),
        ],
        cwd=ROOT,
        env=base_env,
    )

    run_dir = _find_latest_run_dir(
        mlruns_root=mlruns_root,
        experiment_name=experiment_tag,
        start_ts=start_ts,
    )
    model_run_dir = model_dir / "mlflow_run"
    if model_run_dir.exists():
        shutil.rmtree(model_run_dir)
    shutil.copytree(run_dir, model_run_dir)
    try:
        shutil.rmtree(run_dir)
    except Exception:
        pass
    return model_run_dir, experiment_tag


