def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--template", required=True, help="Base workflow YAML template")
    ap.add_argument("--output-root", required=True, help="Root output dir, usually .../model_predict")
    ap.add_argument("--analysis-root", required=True, help="Experiment root dir, used for backtest report output")
    ap.add_argument("--experiment-name", required=True)
    ap.add_argument("--latest-pred-date", default=None)
    ap.add_argument("--uri-folder", default="mlruns")
    ap.add_argument("--half-life", type=int, default=252)
    ap.add_argument("--floor", type=float, default=0.2)
    ap.add_argument("--step-years", type=int, default=1,
                    help="Walk-forward stride in years (annual mode). Overrides --step-weeks when > 0.")
    ap.add_argument("--step-weeks", type=int, default=0,
                    help="Walk-forward stride in weeks (weekly mode, used only when --step-years=0)")
    ap.add_argument("--test-years", type=int, default=3,
                    help="Test window width in years (annual mode only)")
    ap.add_argument("--test-weeks", type=int, default=52,
                    help="Test window width in weeks (weekly mode only; annual mode uses --test-years)")
    ap.add_argument("--train-years", type=int, default=7)  # 7y train window
    ap.add_argument("--valid-years", type=int, default=2)
    ap.add_argument("--history-years", type=int, default=10,
                    help="Annual mode only: roll backward from the latest anchor for at most this many years")
    ap.add_argument("--train-base-start", default="2008-01-01")
    ap.add_argument("--walk-forward-start", default="2008-01-01")
    ap.add_argument("--walk-forward-end", required=True)
    ap.add_argument("--hold-num", type=int, default=int(os.environ.get("HOLD_NUM", "5")))
    ap.add_argument("--segment-years", type=int, default=1, help="Group folds into year chunks for staged execution")
    ap.add_argument("--model-mode", choices=["default", "robust"], default="robust")
    args = ap.parse_args()

    template_path = Path(args.template)
    if not template_path.exists():
        template_str = str(template_path)
        workspace_root = str(ROOT)
        if template_str.startswith(workspace_root):
            template_path = ROOT / template_path.relative_to(workspace_root)
        elif template_str.startswith("/Users/apple/gitee/qlib"):
            rel = Path(template_str[len("/Users/apple/gitee/qlib"):].lstrip("/"))
            template_path = ROOT / rel
        elif template_str.startswith("/work"):
            rel = Path(template_str[len("/work"):].lstrip("/"))
            template_path = ROOT / rel

    if args.step_years <= 0 and args.step_weeks <= 0:
        raise ValueError("Either --step-years or --step-weeks must be positive")
    if args.train_years <= 0 or args.valid_years <= 0:
        raise ValueError("train-years and valid-years must be positive")
    if args.hold_num <= 0:
        raise ValueError("hold-num must be positive")
    if args.half_life <= 0:
        raise ValueError("half-life must be positive")
    if not (0 < args.floor <= 1):
        raise ValueError("floor must be in (0, 1]")

    output_root = Path(args.output_root)
    output_root.mkdir(parents=True, exist_ok=True)
    wf_root = output_root / "walk_forward"
    wf_root.mkdir(parents=True, exist_ok=True)
    mlruns_root = ROOT / args.uri_folder

    env = os.environ.copy()
    fold_rows: list[dict[str, object]] = []
    fold_outputs: list[Path] = []
    latest_fold_output: Path | None = None
    latest_fold_tag: str | None = None
    previous_checkpoints: dict[str, Path] = {}

    walk_forward_end = args.walk_forward_end or args.latest_pred_date
    if not walk_forward_end:
        raise ValueError("walk-forward-end or latest-pred-date must be provided")

    template_cfg, calendar = _load_trade_calendar(
        template_path=template_path,
        uri_folder=args.uri_folder,
        start_date=args.train_base_start,
        end_date=walk_forward_end,
    )
    calendar_end = pd.Timestamp(calendar.max())
    requested_end = pd.Timestamp(walk_forward_end)
    effective_end = min(requested_end, calendar_end)
    effective_end_str = _fmt(effective_end)
    if effective_end < requested_end:
        print(
            f"⚠ Requested walk-forward end {requested_end.date()} exceeds local qlib data. "
            f"Clipped to latest trade date {calendar_end.date()}."
        )

    folds = _build_fold_dates(
        calendar=calendar,
        start_date=args.walk_forward_start,
        end_date=effective_end_str,
        step_years=args.step_years,
        step_weeks=args.step_weeks,
        test_years=args.test_years,
        test_weeks=args.test_weeks,
        train_years=args.train_years,
        valid_years=args.valid_years,
        train_base_start=args.train_base_start,
        history_years=args.history_years,
    )

    _mode = f"{args.step_years}y" if args.step_years > 0 else f"{args.step_weeks}w"
    _test_win = f"{args.test_years} year(s)" if args.step_years > 0 else f"{args.step_weeks} week(s)"
    print("═══════════════════════════════════════════════")
    print("Walk-forward stage2 plan")
    print(f"  window  : train={args.train_years}y  dev={args.valid_years}y  test={_test_win}")
    print(f"  stride  : {_mode}  ({'annual' if args.step_years > 0 else 'weekly'} mode)")
    if args.step_years > 0:
        print(f"  history : {args.history_years} year(s)")
    print(f"  range   : {args.walk_forward_start} -> {effective_end_str}")
    print(f"  data max: {calendar_end.date()}")
    print(f"  segment : {args.segment_years} year(s)")
    for i, fold in enumerate(folds, start=1):
        print(
            f"  [{i:04d}/{len(folds):04d}] signal={fold['signal_date']} pred={fold['pred_date']} test={fold['test_start']} ~ {fold['test_end']} oos={fold['test_start']} ~ {fold['oos_end']} "
            f"train={fold['train_start']} ~ {fold['train_end']} valid={fold['valid_start']} ~ {fold['valid_end']}"
        )
    print("═══════════════════════════════════════════════")

    if not folds:
        raise RuntimeError(
            f"No valid walk-forward folds generated for {args.walk_forward_start} -> {effective_end_str}. "
            "Check train-years/valid-years/test-years against the local qlib calendar coverage."
        )

    segment_groups = _group_folds_by_year(folds, args.segment_years)
    fold_index = 0
    for segment_key, segment_folds in segment_groups:
        segment_dir = wf_root / "segments" / segment_key
        segment_dir.mkdir(parents=True, exist_ok=True)
        print(f"\n════════════ Segment {segment_key} ({len(segment_folds)} folds) ════════════")

        for fold in segment_folds:
            fold_index += 1
            fold_tag = fold["signal_date"]
            fold_dir = wf_root / fold_tag
            fold_dir.mkdir(parents=True, exist_ok=True)
            fold_run_dir = fold_dir / "ensemble_run"
            fold_output = fold_dir / "model_predict"

            print(
                f"\n[{fold_index}/{len(folds)}] Fold {fold_tag}: {fold['train_start']} ~ {fold['train_end']} | "
                f"{fold['valid_start']} ~ {fold['valid_end']} | test={fold['test_start']} ~ {fold['test_end']} | oos={fold['test_start']} ~ {fold['oos_end']}"
            )

            # ① Skip fold if already completed (resume support); regenerate exports if the cached run exists.
            if (not _fold_output_ready(fold_output)) and fold_run_dir.exists():
                print(f"  ↻ Fold {fold_tag}: cached mlflow run found — regenerating missing exports")
                _export_fold_outputs(fold_run_dir, fold_output, fold["pred_date"], env)

            if _fold_output_matches_window(fold_output, fold["pred_date"], fold["oos_end"]):
                print(f"  ↩ Fold {fold_tag}: scores.csv exists — SKIPPED (resuming)")
                metrics_csv = fold_output / "metrics.csv"
                metrics = pd.read_csv(metrics_csv).iloc[0].to_dict() if metrics_csv.exists() else {}
                for spec in MODEL_SPECS:
                    model_name = str(spec["name"])
                    ckpt = fold_dir / "model_runs" / model_name / "mlflow_run" / "artifacts" / "params.pkl"
                    if ckpt.exists():
                        previous_checkpoints[model_name] = ckpt
                metrics.update(
                    {
                        "fold": fold_index,
                        "fold_tag": fold_tag,
                        "segment": segment_key,
                        "signal_date": fold["signal_date"],
                        "train_start": fold["train_start"],
                        "train_end": fold["train_end"],
                        "valid_start": fold["valid_start"],
                        "valid_end": fold["valid_end"],
                        "test_start": fold["test_start"],
                        "test_end": fold["test_end"],
                        "pred_date": fold["pred_date"],
                        "oos_end": fold["oos_end"],
                        "skipped": True,
                    }
                )
                fold_rows.append(metrics)
                fold_outputs.append(fold_output)
                latest_fold_output = fold_output
                latest_fold_tag = fold_tag
                continue
            elif _fold_output_ready(fold_output):
                print(f"  ↻ Fold {fold_tag}: cached outputs do not match target OOS horizon — retraining")
            metrics = _build_ensemble_fold_output(
                fold=fold,
                fold_dir=fold_dir,
                fold_output=fold_output,
                mlruns_root=mlruns_root,
                template_cfg=template_cfg,
                base_env=env,
                args=args,
                previous_checkpoints=previous_checkpoints,
            )
            for spec in MODEL_SPECS:
                model_name = str(spec["name"])
                ckpt = fold_dir / "model_runs" / model_name / "mlflow_run" / "artifacts" / "params.pkl"
                if ckpt.exists():
                    previous_checkpoints[model_name] = ckpt
            metrics.update(
                {
                    "fold": fold_index,
                    "fold_tag": fold_tag,
                    "segment": segment_key,
                    "signal_date": fold["signal_date"],
                    "train_start": fold["train_start"],
                    "train_end": fold["train_end"],
                    "valid_start": fold["valid_start"],
                    "valid_end": fold["valid_end"],
                    "test_start": fold["test_start"],
                    "test_end": fold["test_end"],
                    "pred_date": fold["pred_date"],
                    "oos_end": fold["oos_end"],
                }
            )
            fold_rows.append(metrics)
            fold_outputs.append(fold_output)
            latest_fold_output = fold_output
            latest_fold_tag = fold_tag

        # per-segment summary for resumability / inspection
        segment_rows = [row for row in fold_rows if row.get("segment") == segment_key]
        if segment_rows:
            segment_df = pd.DataFrame(segment_rows)
            segment_df.to_csv(segment_dir / "segment_folds.csv", index=False, encoding="utf-8-sig")
            (segment_dir / "segment_summary.txt").write_text(
                f"Segment {segment_key}\nFolds: {len(segment_rows)}\nLatest fold: {segment_rows[-1].get('fold_tag')}\n",
                encoding="utf-8",
            )

    if latest_fold_output is None or latest_fold_tag is None:
        raise RuntimeError("No walk-forward folds were completed")

    # Preserve the latest fold outputs at the root for stage3/4/5/6.
    _copy_fold_outputs(latest_fold_output, output_root)

    # Save fold-level and aggregate reports.
    _generate_summary(fold_rows, output_root)

    # Full-cycle consolidated backtest from all weekly fold signals.
    _write_full_backtest_report(output_root, Path(args.analysis_root), fold_rows, fold_outputs, template_cfg, args.uri_folder, args.hold_num)

    print("\n═══════════════════════════════════════════════")
    print(f"Latest fold copied to root output: {latest_fold_tag}")
    print(f"Walk-forward fold summary: {output_root / 'walk_forward_folds.csv'}")
    print(f"Walk-forward aggregate summary: {output_root / 'walk_forward_summary.csv'}")
    print(f"Full-cycle backtest report: {Path(args.analysis_root) / 'report_of_backtest.txt'}")
    print("═══════════════════════════════════════════════")


if __name__ == "__main__":
    main()