def _generate_summary(fold_rows: list[dict[str, object]], output_root: Path) -> None:
    if not fold_rows:
        return

    fold_df = pd.DataFrame(fold_rows)
    fold_csv = output_root / "walk_forward_folds.csv"
    fold_df.to_csv(fold_csv, index=False, encoding="utf-8-sig")

    ic_source = fold_df["IC_mean"] if "IC_mean" in fold_df else pd.Series(dtype=float)
    ic_series = pd.to_numeric(ic_source, errors="coerce").dropna()
    ic_mean = float(ic_series.mean()) if not ic_series.empty else np.nan
    ic_std = float(ic_series.std(ddof=1)) if len(ic_series) > 1 else np.nan
    ic_t_stat = (
        float(ic_mean / (ic_std / np.sqrt(len(ic_series))))
        if len(ic_series) > 1 and pd.notna(ic_std) and ic_std > 0
        else np.nan
    )
    skipped_count = int(fold_df["skipped"].fillna(False).astype(bool).sum()) if "skipped" in fold_df else 0
    executed_count = int(len(fold_df) - skipped_count)
    available_count = int(len(fold_df))

    metric_cols = [
        c
        for c in fold_df.columns
        if pd.api.types.is_numeric_dtype(fold_df[c]) and c not in {"fold"}
    ]
    summary_rows = []
    for metric in metric_cols:
        series = pd.to_numeric(fold_df[metric], errors="coerce").dropna()
        summary_rows.append(
            {
                "metric": metric,
                "mean": float(series.mean()) if not series.empty else np.nan,
                "std": float(series.std()) if not series.empty else np.nan,
                "min": float(series.min()) if not series.empty else np.nan,
                "max": float(series.max()) if not series.empty else np.nan,
                "last": float(series.iloc[-1]) if not series.empty else np.nan,
            }
        )
    summary_rows.extend(
        [
            {"metric": "fold_count", "mean": float(available_count), "std": np.nan, "min": np.nan, "max": np.nan, "last": float(available_count)},
            {"metric": "available_fold_count", "mean": float(available_count), "std": np.nan, "min": np.nan, "max": np.nan, "last": float(available_count)},
            {"metric": "executed_fold_count", "mean": float(executed_count), "std": np.nan, "min": np.nan, "max": np.nan, "last": float(executed_count)},
            {"metric": "cached_fold_count", "mean": float(skipped_count), "std": np.nan, "min": np.nan, "max": np.nan, "last": float(skipped_count)},
            {"metric": "IC_mean_t_stat", "mean": ic_t_stat, "std": np.nan, "min": np.nan, "max": np.nan, "last": ic_t_stat},
        ]
    )
    summary_df = pd.DataFrame(summary_rows)
    summary_df.to_csv(output_root / "walk_forward_summary.csv", index=False, encoding="utf-8-sig")

    # Simple human-readable report for quick inspection.
    report_lines = [
        "Walk-forward Stage2 Summary",
        "===========================",
        f"Available folds: {available_count}",
        f"Executed folds this run: {executed_count}",
        f"Resumed cached folds: {skipped_count}",
        f"IC mean: {ic_mean:.6f}" if pd.notna(ic_mean) else "IC mean: N/A",
        f"IC t-stat: {ic_t_stat:.6f}" if pd.notna(ic_t_stat) else "IC t-stat: N/A",
        "",
        "Per-fold metrics (top rows):",
        fold_df.head(20).to_string(index=False),
        "",
        "Aggregate metrics:",
        summary_df.to_string(index=False),
        "",
    ]
    (output_root / "walk_forward_summary.txt").write_text("\n".join(report_lines), encoding="utf-8")


def _load_fold_scores(fold_output: Path) -> pd.DataFrame:
    """Load per-fold prediction scores for full-cycle backtest aggregation.

    Prefers all_scores.csv (all test-period dates) over scores.csv (single
    snapshot date).  all_scores.csv is written by export_model_predict.py
    when pred.pkl contains multiple trading days (annual mode).
    """
    all_scores_csv = fold_output / "all_scores.csv"
    scores_csv = fold_output / "scores.csv"
    csv_path = all_scores_csv if all_scores_csv.exists() else scores_csv
    if not csv_path.exists():
        return pd.DataFrame()

    df = pd.read_csv(csv_path)
    if df.empty:
        return df

    # Normalise column names: accept both 'pred_date' and 'datetime'
    date_col = next((c for c in ["datetime", "pred_date"] if c in df.columns), None)
    if date_col is None or "instrument" not in df.columns or "score" not in df.columns:
        return pd.DataFrame()

    out = df[[date_col, "instrument", "score"]].copy()
    out["datetime"] = pd.to_datetime(out[date_col])
    out = out[["datetime", "instrument", "score"]]
    out["score"] = pd.to_numeric(out["score"], errors="coerce")
    out = out.dropna(subset=["datetime", "instrument", "score"])
    return out


def _build_full_signal(fold_outputs: list[Path]) -> pd.DataFrame:
    frames: list[pd.DataFrame] = []
    for fold_output in fold_outputs:
        df = _load_fold_scores(fold_output)
        if not df.empty:
            frames.append(df)

    if not frames:
        return pd.DataFrame(columns=["score"])

    signal_df = pd.concat(frames, ignore_index=True)
    signal_df = signal_df.drop_duplicates(subset=["datetime", "instrument"], keep="last")
    signal_df = signal_df.sort_values(["datetime", "instrument"])
    signal_df = signal_df.set_index(["datetime", "instrument"])[["score"]]
    signal_df.index = signal_df.index.set_names(["datetime", "instrument"])
    return signal_df


def _backtest_metrics_from_report(report_df: pd.DataFrame) -> dict[str, float]:
    if report_df is None or report_df.empty:
        return {
            "annualized_return": float("nan"),
            "max_drawdown": float("nan"),
            "sharpe_ratio": float("nan"),
            "information_ratio": float("nan"),
            "monthly_win_rate": float("nan"),
            "turnover_mean": float("nan"),
            "turnover_sum": float("nan"),
        }

    rpt = report_df.copy()
    rpt.index = pd.to_datetime(rpt.index)
    for col in ["return", "cost", "bench", "turnover"]:
        if col not in rpt.columns:
            rpt[col] = 0.0
        rpt[col] = pd.to_numeric(rpt[col], errors="coerce").fillna(0.0)

    strategy_ret = rpt["return"] - rpt["cost"]
    excess_ret = strategy_ret - rpt["bench"]
    n = len(strategy_ret)
    annualized_return = float((1 + strategy_ret).prod() ** (252 / max(n, 1)) - 1) if n else float("nan")
    equity = (1 + strategy_ret).cumprod()
    max_drawdown = float((equity / equity.cummax() - 1).min()) if n else float("nan")
    sharpe = float(strategy_ret.mean() / strategy_ret.std(ddof=1) * np.sqrt(252)) if strategy_ret.std(ddof=1) > 0 else float("nan")
    ir = float(excess_ret.mean() / excess_ret.std(ddof=1) * np.sqrt(252)) if excess_ret.std(ddof=1) > 0 else float("nan")
    monthly = (1 + strategy_ret).resample("ME").prod() - 1 if n else pd.Series(dtype=float)

    return {
        "annualized_return": annualized_return,
        "max_drawdown": max_drawdown,
        "sharpe_ratio": sharpe,
        "information_ratio": ir,
        "monthly_win_rate": float((monthly > 0).mean()) if not monthly.empty else float("nan"),
        "turnover_mean": float(rpt["turnover"].mean()),
        "turnover_sum": float(rpt["turnover"].sum()),
    }


def _signal_coverage_diagnostics(trade_signal: pd.DataFrame, calendar: pd.DatetimeIndex) -> dict[str, float]:
    if trade_signal.empty:
        return {
            "trade_signal_dates": 0.0,
            "calendar_days": 0.0,
            "signal_coverage_ratio": float("nan"),
            "median_gap_days": float("nan"),
            "max_gap_days": float("nan"),
        }

    signal_dates = pd.DatetimeIndex(pd.to_datetime(trade_signal.index.get_level_values("datetime")).unique()).sort_values()
    calendar_slice = calendar[(calendar >= signal_dates.min()) & (calendar <= signal_dates.max())]
    coverage_ratio = float(len(signal_dates) / len(calendar_slice)) if len(calendar_slice) else float("nan")

    if len(signal_dates) > 1:
        calendar_pos = calendar.get_indexer(signal_dates)
        gap_sizes = np.diff(calendar_pos)
        median_gap_days = float(np.median(gap_sizes))
        max_gap_days = float(np.max(gap_sizes))
    else:
        median_gap_days = float("nan")
        max_gap_days = float("nan")

    return {
        "trade_signal_dates": float(len(signal_dates)),
        "calendar_days": float(len(calendar_slice)),
        "signal_coverage_ratio": coverage_ratio,
        "median_gap_days": median_gap_days,
        "max_gap_days": max_gap_days,
    }


