def _fold_output_matches_window(fold_output: Path, pred_date: str, oos_end: str) -> bool:
    if not _fold_output_ready(fold_output):
        return False

    scores_csv = fold_output / "scores.csv"
    all_scores_csv = fold_output / "all_scores.csv"
    if not scores_csv.exists() or not all_scores_csv.exists():
        return False

    metadata_json = fold_output / "ensemble_metadata.json"
    expected_models = [spec["name"] for spec in MODEL_SPECS]
    if metadata_json.exists():
        try:
            metadata = json.loads(metadata_json.read_text(encoding="utf-8"))
            actual_models = metadata.get("models")
            if actual_models is not None and list(actual_models) != expected_models:
                return False
            if metadata.get("warm_start_strategy") != WARM_START_STRATEGY:
                return False
        except Exception:
            return False

    try:
        scores_df = pd.read_csv(scores_csv, usecols=lambda c: c in {"pred_date"})
        if scores_df.empty or "pred_date" not in scores_df.columns:
            return False
        score_pred_date = str(scores_df["pred_date"].dropna().astype(str).iloc[0])
        if score_pred_date != pred_date:
            return False

        all_scores_df = pd.read_csv(all_scores_csv, usecols=lambda c: c in {"datetime"})
        if all_scores_df.empty or "datetime" not in all_scores_df.columns:
            return False
        max_dt = pd.to_datetime(all_scores_df["datetime"], errors="coerce").dropna().max()
        if pd.isna(max_dt):
            return False
        return max_dt >= pd.Timestamp(oos_end)
    except Exception:
        return False


